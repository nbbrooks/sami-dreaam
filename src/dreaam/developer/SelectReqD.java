    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        for (RequirementSpecification r : mapping.keySet()) {
            JCheckBox cb = mapping.get(r);
            if (!r.isFilled() && cb.isSelected()) {
                r.setFilled(true);
                r.setFilledBy(o);
            } else if (r.isFilled() && !cb.isSelected() && r.getFilledBy() == o) {
                r.setFilled(false);
                r.setFilledBy(null);
            }
        }
        setVisible(false);
    }//GEN-LAST:event_jButton1ActionPerformed

