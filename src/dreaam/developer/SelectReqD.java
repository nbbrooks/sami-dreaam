    public SelectReqD(java.awt.Frame parent, boolean modal, Object o) {
        super(parent, modal);
        initComponents();
        setTitle("SelectReqD");
        this.o = o;
        reqP.setLayout(new GridLayout(0, 1));
        Mediator mediator = new Mediator();
        if (mediator.getProjectSpec().getReqs() != null) {
            for (RequirementSpecification r : mediator.getProjectSpec().getReqs()) {
                JCheckBox cb = new JCheckBox(r.toString());
                mapping.put(r, cb);
                cb.setSelected(r.isFilled());
                if (r.isFilled() && (o == r.getFilledBy())) {
                    cb.setEnabled(false);
                }
                reqP.add(cb);
            }
        }
        reqP.revalidate();
    }

