    private void okBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okBActionPerformed
        // TODO add your handling code here:

        if (uniqueCB.isSelected()) {
            unique = true;
        } else {
            sources.addAll(new ArrayList<Place>());

        }

        nPlaces = (Integer) numberSP.getValue();

        if (nPlaces == 0) {
            nPlaces = 1;
        }

        JOptionPane.showMessageDialog(null, "TEST: " + unique + "; " + nPlaces);
        setVisible(false);
    }//GEN-LAST:event_okBActionPerformed

